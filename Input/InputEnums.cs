namespace DerpySimulation.Input
{
    /// <summary>An enum of keys that have a function. Text input (chars) is handled separately.</summary>
    internal enum Key : byte
    {
        Escape,
        W,
        A,
        S,
        D,
        LShift,
        Space
    }
    internal enum MouseButton : byte
    {
        Left,
        Middle,
        Right,
        X1,
        X2
    }
}
